/**
 * 注音標註:用原生 <ruby> 把注音顯示在字的上方。
 * 每段文字以 [字, 注音] 的陣列定義,注音留空字串的字(標點、數字)原樣輸出。
 * 變調依台灣教科書慣例標實際讀音(一題 ㄧˋ、不扣 ㄅㄨˊ)。
 */

export type ZyPair = [string, string]

export function Zy({ t }: { t: ZyPair[] }) {
  return (
    <>
      {t.map(([c, z], i) =>
        z ? (
          <ruby key={i}>
            {c}
            <rt>{z}</rt>
          </ruby>
        ) : (
          <span key={i}>{c}</span>
        ),
      )}
    </>
  )
}

